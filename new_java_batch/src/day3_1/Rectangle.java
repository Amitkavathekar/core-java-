package day3_1;

public class Rectangle {
	 private int width;
	 private int height;
	 
	public Rectangle() {
		
	}

	public Rectangle(int width, int height) {
		this.width = width;
		this.height = height;
	}
	 public void printdetails() {
		 System.out.println("width:" +width +"height: " +height);
		
	} 
}
